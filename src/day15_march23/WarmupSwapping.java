package day15_march23;

public class WarmupSwapping {

	public static void main(String[] args) {
		
		int x = 10;
		int y = 20;
		
		int temp ;
		
		temp = x; //10
		x = y ; //20
		y = temp; //10
		
		System.out.println("New x is: " + x);
		System.out.println("New y is: " +y);
	
		
		
		
		
		
		
		
		
		
		
		

	}

}
