package day15_march23;

public class NumToChar {

	public static void main(String[] args) {

		int x = 100;
		char myChar = (char) x;
		System.out.println(myChar);
		
		//from lower to higher order in ASCII table
		//special character -->> number -->> uppercase -->> lowercase
		
		double y = 100.99;
		char myChar2 = (char) y ;
		System.out.println(myChar2);
		
		
		
		
		
	}

}
