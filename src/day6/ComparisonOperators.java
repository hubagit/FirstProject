package day6;

public class ComparisonOperators {

	public static void main(String[] args) {
		
		int a = 10, b = 11;
		
		System.out.println("Is a and b equal? " + (a == b));

		System.out.println("Is a and b equal? " + (a != b));
		
		System.out.println("Is a and b greater than or equal? " + (a >= b));
		
		System.out.println("Is a and b greater than equal? " + (a > b));
		
		System.out.println("Is a and b less than or equal? " + (a <= b));
		
		System.out.println("Is a and b less than? " + (a < b));
		
		boolean isAequaltoB = (a == b);
	}

}
