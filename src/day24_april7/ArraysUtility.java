package day24_april7;

import java.util.Arrays;

public class ArraysUtility {

	public static void main(String[] args) {

		int[] nums = new int[] { 1, 45, 33, 100, 8 };

		System.out.println(nums);

		// Array is an object that stores multiple items of same type
		// Arrays is utility class that come with JDK just like scanner
		// Arrays class is in java.ytil package
		// it provides lots of predefined methods to work with
		// array objects

//		System.out.println(Arrays.toString(nums));

		String str = Arrays.toString(nums);
		System.out.println(str);
		
	}

}
